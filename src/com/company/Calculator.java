package com.company;

public class Calculator {
    public Calculator(){}
    public double add( double term1, double term2 ){ return term1 + term2; }
    public double sub( double term1, double term2 ){ return term1 - term2; }
    public double multiply( double term1, double term2 ){ return term1 * term2; }
    public double divide( double term1, double term2 ){ return term1 / term2; }
}
